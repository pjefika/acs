package entidades.lanHost;

public class LanHostHolder {
	
	private String iftype;

	private String hostmac;

	private String status;

	private String ltremain;

	private String hostname;

	private String hostip;
	
	private String hostinst;

	public String getIftype() {
		return iftype;
	}

	public void setIftype(String iftype) {
		this.iftype = iftype;
	}

	public String getHostmac() {
		return hostmac;
	}

	public void setHostmac(String hostmac) {
		this.hostmac = hostmac;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLtremain() {
		return ltremain;
	}

	public void setLtremain(String ltremain) {
		this.ltremain = ltremain;
	}

	public String getHostname() {
		return hostname;
	}

	public void setHostname(String hostname) {
		this.hostname = hostname;
	}

	public String getHostip() {
		return hostip;
	}

	public void setHostip(String hostip) {
		this.hostip = hostip;
	}

	public String getHostinst() {
		return hostinst;
	}

	public void setHostinst(String hostinst) {
		this.hostinst = hostinst;
	}	
	
}
