package entidades.interfaceStatics;

public class Values {
	
	private String name;
	
	private Boolean enabled;
	
	private Long bytesReceived;
	
	private Boolean status;
	
	private Long errorsReceived;
	
	private String type;
	
	private Long errorsSent;
	
	private Long packetsReceived;
	
	private String ip;
	
	private Long packetsSent;
	
	private Long bytesSent;
	
	private String mac;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Boolean getEnabled() {
		return enabled;
	}

	public void setEnabled(Boolean enabled) {
		this.enabled = enabled;
	}

	public Long getBytesReceived() {
		return bytesReceived;
	}

	public void setBytesReceived(Long bytesReceived) {
		this.bytesReceived = bytesReceived;
	}

	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}

	public Long getErrorsReceived() {
		return errorsReceived;
	}

	public void setErrorsReceived(Long errorsReceived) {
		this.errorsReceived = errorsReceived;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Long getErrorsSent() {
		return errorsSent;
	}

	public void setErrorsSent(Long errorsSent) {
		this.errorsSent = errorsSent;
	}

	public Long getPacketsReceived() {
		return packetsReceived;
	}

	public void setPacketsReceived(Long packetsReceived) {
		this.packetsReceived = packetsReceived;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public Long getPacketsSent() {
		return packetsSent;
	}

	public void setPacketsSent(Long packetsSent) {
		this.packetsSent = packetsSent;
	}

	public Long getBytesSent() {
		return bytesSent;
	}

	public void setBytesSent(Long bytesSent) {
		this.bytesSent = bytesSent;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}	

}
